<h1>Contest programming</h1>
<p>I occasionally compete in
<a href="http://community.topcoder.com/tc?module=MemberProfile&cr=22696982">TopCoder</a>
and <a href="http://codeforces.com/profile/bbi5291">Codeforces</a>. I've solved
a lot of problems on SPOJ
(<a href="http://www.spoj.com/users/bbi5291">profile</a>), and almost all of my
SPOJ solutions can be found on GitHub
<a href="https://github.com/t3nsor/SPOJ/">here</a>. I hope you don't copy my
solutions and submit them as your own (although technically it is legal to do
so since there's nothing in the license that forbids it), because the only
person you'll be cheating is yourself. On the other hand, feel free to look at
them if you're stuck on a particular problem.</p>
<p>If you're an ACM-ICPC contestant, and you don't have a team notebook
(reference material of 25 printed pages or less, to bring with you to the
competition floor) then you should consider creating one! It can occasionally
be extremely useful, such as during the 2013 University of Chicago
Invitational, where my team (University of Toronto) took first place. Carnegie
Mellon University would certainly have beaten us, but for the fact that they
were unable to implement the simplex algorithm, while we had a pre-written
version in our notebook. An ACM-ICPC reference notebook similar to the one I
used as a contestant can be found on my GitHub, <a
href="https://github.com/t3nsor/codebook">here</a>. I encourage you to take
a look to get an idea of what sort of things would be useful to include when
preparing your own team notebook (and what algorithms and techniques you might
have missed, if you already have one.) You can even copy this notebook in part
or in whole.</p>
<p>I run the <a href="http://wcipeg.com">PEG Judge</a>. I'm clearly biased, but
I believe the PEG Judge is probably the best online judge to use if you're new
to competitive programming, because it has a large number of problems that are
<em>really</em> easy, as well as IOI-level problems and everything in between!
It also supports many languages (not as many as SPOJ, though) and has analyses
for many problems. The history of the PEG Judge is documented on the PEG Wiki,
<a href="http://wcipeg.com/wiki/Judge:History">here</a>. The
<a href="http://wcipeg.com/wiki">PEG Wiki</a> is a companion site to the PEG
Judge. It is used for hosting expository articles about computer science
(with a focus on competitive programming techniques) as well as for documenting
the PEG Judge. I encourage you to contribute, as I clearly have neither the
free time nor the expertise to write an entire wiki by myself.</p>
