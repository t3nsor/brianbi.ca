<h1>Other projects</h1>
<p>My GitHub profile is located
<a href="https://github.com/t3nsor">here</a>.</p>
<p>In 2011 I was an intern at Facebook, Inc. and spent a lot of time
procrastinating on the internal IRC network. I enjoy playing the game Mafia, so
I decided to look for an IRC bot that could moderate Mafia games over IRC.
Unfortunately, none of the ones I found actually worked, so I decided to
develop my own. I named it Kevinbot, and you can find the code
<a href="https://github.com/t3nsor/kevinbot">here</a>. My original plan was
that it would be easily extensible, just by deriving from some sort of
Character class and overriding the behaviour in order to create new roles.
Unfortunately, the summer ended before I was able to finish developing it in
all its glory. Nevertheless, the current version on GitHub is fully functional;
documentation (including an explanation of the name) can be found on the wiki
attached to the GitHub repo.</p>
<p>The other project you'll notice is a little program called ttycat. I don't
think it has any practical application; its purpose is to illustrate the
Unix terminal device API and facilitate experimenting with various terminal
attributes. It makes more sense if you read my seven-part introduction to
terminal devices, starting
<a href="http://spin0r.wordpress.com/2012/12/16/terminally-confused-part-one/">here</a>. (I wrote this just as much for myself as for anyone else; the official
sources of documentation can sometimes be a bit ambiguous, so I worked out a
lot of details myself and wrote them down so I could refer to them later.)</p>
